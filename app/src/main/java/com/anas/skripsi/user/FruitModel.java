package com.anas.skripsi.user;

public class FruitModel {
    String fruit_name;
    String fruit_image_urls;

    public FruitModel() {
        this.fruit_name = fruit_name;
        this.fruit_image_urls = fruit_image_urls;
    }

    public String getFruit_name() {
        return fruit_name;
    }

    public void setFruit_name(String fruit_name) {
        this.fruit_name = fruit_name;
    }

    public String getFruit_image_urls() {
        return fruit_image_urls;
    }

    public void setFruit_image_urls(String fruit_image_urls) {
        this.fruit_image_urls = fruit_image_urls;
    }
}

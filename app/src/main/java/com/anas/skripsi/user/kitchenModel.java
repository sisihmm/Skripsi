package com.anas.skripsi.user;

public class kitchenModel {
    String kitchen_name;
    String kitchen_img;

    public kitchenModel() {
        this.kitchen_name = kitchen_name;
        this.kitchen_img = kitchen_img;
    }

    public String getKitchen_name() {
        return kitchen_name;
    }

    public void setKitchen_name(String kitchen_name) {
        this.kitchen_name = kitchen_name;
    }

    public String getKitchen_img() {
        return kitchen_img;
    }

    public void setKitchen_img(String kitchen_img) {
        this.kitchen_img = kitchen_img;
    }
}

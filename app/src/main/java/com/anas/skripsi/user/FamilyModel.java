package com.anas.skripsi.user;

public class FamilyModel {
    String img_family;
    String name_family;

    public FamilyModel() {
        this.img_family = img_family;
        this.name_family = name_family;
    }

    public String getImg_family() {
        return img_family;
    }

    public void setImg_family(String img_family) {
        this.img_family = img_family;
    }

    public String getName_family() {
        return name_family;
    }

    public void setName_family(String name_family) {
        this.name_family = name_family;
    }
}
